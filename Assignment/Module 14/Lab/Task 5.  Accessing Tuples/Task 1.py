#Accessing tuple elements using positive and negative indexing.

colors = ('red', 'green', 'blue', 'yellow', 'orange', 'purple')
print("total colors",colors)


# Positive indexing 
print("Positive indexing:")
print("colors[0]:", colors[0])    
print("colors[2]:", colors[2])    
print("colors[5]:", colors[5])  

# Negative indexing 
print("\nNegative indexing:")
print("colors[-1]:", colors[-1])  
print("colors[-3]:", colors[-3])  
print("colors[-6]:", colors[-6])  